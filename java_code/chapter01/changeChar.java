public class changeChar {
	public static void main(String[] args) {
		System.out.println("转义字符");
		//    \t : 一个制表位
		System.out.println("制\t表\t位");
		//   \n 换行符
		System.out.println("换\n行");
		// \\ 一个\
		System.out.println("D:\\java\\learn_java\\java_code");
		// \"   
		System.out.println("说:\"真好\"");
		// \r 表示一个回车
		System.out.println("回车了吗?\r是的吧");
	}
}